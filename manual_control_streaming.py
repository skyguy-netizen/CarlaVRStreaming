    def _parse_vehicle_keys(self, keys, milliseconds):
        self._control.throttle = 1.0 if keys[K_UP] or keys[K_w] else 0.0
        steer_increment = 5e-4 * milliseconds
        if keys[K_LEFT] or keys[K_a]:
            self._steer_cache -= steer_increment
        elif keys[K_RIGHT] or keys[K_d]:
            self._steer_cache += steer_increment
        else:
            self._steer_cache = 0.0
        self._steer_cache = min(0.7, max(-0.7, self._steer_cache))
        self._control.steer = round(self._steer_cache, 1)
        self._control.brake = 1.0 if keys[K_DOWN] or keys[K_s] else 0.0
        self._control.hand_brake = keys[K_SPACE]

    def _parse_vehicle_wheel(self, frame):
        numAxes = self._joystick.get_numaxes()
        jsInputs = [float(self._joystick.get_axis(i)) for i in range(numAxes)]
        jsButtons = [float(self._joystick.get_button(i)) for i in
                     range(self._joystick.get_numbuttons())]

        # Custom function to map range of inputs [1, -1] to outputs [0, 1] i.e 1 from inputs means nothing is pressed
        # For the steering, it seems fine as it is
        K1 = 1.0  # 0.55
        # steerCmd = K1 * math.tan(1.1 * jsInputs[self._steer_idx])

        # if self._mph < 20:
        #     sensitivity = self.steering_sensitivity_min
        # elif 20 <= self._mph < 65:
        #     sensitivity = (self._mph - 20) / (65 - 20) * (self.steering_sensitivity_max - self.steering_sensitivity_min)
        # else:
        #     sensitivity = self.steering_sensitivity_max
        sensitivity = self.steering_sensitivity_max

        steerCmd = jsInputs[self._steer_idx] * sensitivity

        K2 = 1.6  # 1.6
        throttleCmd = K2 + (2.05 * math.log10(
            -0.7 * jsInputs[self._throttle_idx] + 1.4) - 1.2) / 0.92
        if throttleCmd <= 0:
            throttleCmd = 0
        elif throttleCmd > 1:
            throttleCmd = 1

        brakeCmd = 1.6 + (2.05 * math.log10(
            -0.7 * jsInputs[self._brake_idx] + 1.4) - 1.2) / 0.92
        if brakeCmd <= 0:
            brakeCmd = 0
        elif brakeCmd > 1:
            brakeCmd = 1

        self._control.steer = steerCmd
        self._control.brake = brakeCmd
        self._control.throttle = throttleCmd

        timestamp = pygame.time.get_ticks()

        recorder.save_joystick("THROTTLE", jsInputs[self._throttle_idx], throttleCmd, frame, timestamp)
        recorder.save_joystick("BRAKE", jsInputs[self._brake_idx], brakeCmd, frame, timestamp)
        recorder.save_joystick("STEER", jsInputs[self._steer_idx], steerCmd, frame, timestamp)

        # toggle = jsButtons[self._reverse_idx]

        self._control.hand_brake = bool(jsButtons[self._handbrake_idx])

    def update_steering_config(self, steering_config):
        self.steering_mode = steering_config[0]

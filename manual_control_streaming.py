# WebSocket handler functions
def new_client(client, server):
    print("WebSocket client connected")
    clients.append(client)

def client_left(client, server):
    print("WebSocket client disconnected")
    if client in clients:
        clients.remove(client)

def message_received(client, server, message):
    print(f"Received message: {message}")


last_sent_time = 0

def send_camera_feed():
    if not clients:
        return

    jpeg_data = None
    global last_sent_time
    now = time.time()
    if now - last_sent_time < 1/90:  # 10 FPS
        return
    last_sent_time = now

    # Always get the most recent frame only
    while not image_queue.empty():
        try:
            jpeg_data = image_queue.get_nowait()
        except queue.Empty:
            return

    # Only send the latest image
    if jpeg_data:
        encoded = base64.b64encode(jpeg_data).decode("utf-8")
        message = json.dumps({"type": "frame", "data": encoded})
        for client in clients:
            websocket_server.send_message(client, message)

